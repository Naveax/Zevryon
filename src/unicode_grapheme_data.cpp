constexpr std::uint8_t hex_digit(char value) noexcept {
    return value >= '0' && value <= '9'
               ? static_cast<std::uint8_t>(value - '0')
           : value >= 'a' && value <= 'f'
               ? static_cast<std::uint8_t>(value - 'a' + 10)
               : 0U;
}

template <std::size_t Bytes>
consteval std::array<std::uint8_t, Bytes> decode_hex(std::string_view text) {
    std::array<std::uint8_t, Bytes> output{};
    for (std::size_t index = 0U; index < Bytes; ++index) {
        output[index] = static_cast<std::uint8_t>(
            (hex_digit(text[index * 2U]) << 4U) |
            hex_digit(text[index * 2U + 1U]));
    }
    return output;
}

template <std::size_t Size>
constexpr std::uint32_t read_uleb(
    const std::array<std::uint8_t, Size>& bytes,
    std::size_t* cursor) noexcept {
    std::uint32_t result = 0U;
    std::uint32_t shift = 0U;
    while (*cursor < bytes.size()) {
        const std::uint8_t byte = bytes[*cursor];
        ++(*cursor);
        result |= static_cast<std::uint32_t>(byte & 0x7fU) << shift;
        if ((byte & 0x80U) == 0U) {
            return result;
        }
        shift += 7U;
    }
    return result;
}

template <typename Value, std::size_t Count, std::size_t Size>
consteval std::array<PropertyRange<Value>, Count> decode_property_ranges(
    const std::array<std::uint8_t, Size>& bytes) {
    std::array<PropertyRange<Value>, Count> output{};
    std::size_t cursor = 0U;
    std::uint32_t previous_end = 0xffffffffU;
    for (std::size_t index = 0U; index < Count; ++index) {
        const std::uint32_t gap = read_uleb(bytes, &cursor);
        const std::uint32_t length = read_uleb(bytes, &cursor);
        const std::uint32_t first =
            previous_end == 0xffffffffU ? gap : previous_end + 1U + gap;
        const std::uint32_t last = first + length;
        output[index] = {
            first,
            last,
            static_cast<Value>(bytes[cursor]),
        };
        ++cursor;
        previous_end = last;
    }
    return output;
}

template <std::size_t Count, std::size_t Size>
consteval std::array<BooleanRange, Count> decode_boolean_ranges(
    const std::array<std::uint8_t, Size>& bytes) {
    std::array<BooleanRange, Count> output{};
    std::size_t cursor = 0U;
    std::uint32_t previous_end = 0xffffffffU;
    for (std::size_t index = 0U; index < Count; ++index) {
        const std::uint32_t gap = read_uleb(bytes, &cursor);
        const std::uint32_t length = read_uleb(bytes, &cursor);
        const std::uint32_t first =
            previous_end == 0xffffffffU ? gap : previous_end + 1U + gap;
        const std::uint32_t last = first + length;
        ++cursor;
        output[index] = {first, last};
        previous_end = last;
    }
    return output;
}

