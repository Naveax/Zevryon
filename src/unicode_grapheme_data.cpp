template <typename Value, std::size_t Size>
constexpr Value lookup(
    const std::array<PropertyRange<Value>, Size>& ranges,
    std::uint32_t code_point,
    Value fallback) noexcept {
    std::size_t low = 0U;
    std::size_t high = ranges.size();
    while (low < high) {
        const std::size_t middle = low + (high - low) / 2U;
        const auto& range = ranges[middle];
        if (code_point < range.first) {
            high = middle;
        } else if (code_point > range.last) {
            low = middle + 1U;
        } else {
            return range.value;
        }
    }
    return fallback;
}

template <std::size_t Size>
constexpr bool contains(
    const std::array<BooleanRange, Size>& ranges,
    std::uint32_t code_point) noexcept {
    std::size_t low = 0U;
    std::size_t high = ranges.size();
    while (low < high) {
        const std::size_t middle = low + (high - low) / 2U;
        const auto& range = ranges[middle];
        if (code_point < range.first) {
            high = middle;
        } else if (code_point > range.last) {
            low = middle + 1U;
        } else {
            return true;
        }
    }
    return false;
}

constexpr GraphemeBreakClass hangul_class(std::uint32_t code_point) noexcept {
    if (code_point < 0xac00U || code_point > 0xd7a3U) {
        return GraphemeBreakClass::Other;
    }
    return (code_point - 0xac00U) % 28U == 0U
               ? GraphemeBreakClass::LV
               : GraphemeBreakClass::LVT;
}

