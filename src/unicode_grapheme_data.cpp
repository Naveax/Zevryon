GraphemeProperties grapheme_properties(std::uint32_t code_point) noexcept {
    if (code_point > 0x10ffffU) {
        return {};
    }
    GraphemeBreakClass break_class = hangul_class(code_point);
    if (break_class == GraphemeBreakClass::Other) {
        break_class = lookup(
            kBreakRanges,
            code_point,
            GraphemeBreakClass::Other);
    }
    return {
        break_class,
        lookup(
            kIndicRanges,
            code_point,
            IndicConjunctBreak::None),
        contains(kExtendedPictographicRanges, code_point),
    };
}

